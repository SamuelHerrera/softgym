    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        new Thread() {
            @Override
            public void run() {
                try {
                    Process p = new ProcessBuilder(".\\StillCap.exe", "/device:1", "/clipboard", "/delay:1", "/resolution:160x120").start();
                    p.waitFor();
                    // Obtiene el Clipboard del sistema
                    Clipboard cb = Toolkit.getDefaultToolkit().getSystemClipboard();

                    // Obtiene el contenido
                    Transferable t = cb.getContents(null);
                    if (t == null) {
                        return;
                    }

                    if (t.isDataFlavorSupported(DataFlavor.imageFlavor)) {
                        imagen = (Image) t.getTransferData(DataFlavor.imageFlavor);
                        jLabel6.setIcon(new ImageIcon(imagen.getScaledInstance(122, 161, Image.SCALE_DEFAULT)));
                    }

                } catch (IOException | InterruptedException | UnsupportedFlavorException ex) {
                    Logger.getLogger(Editar_Registro_Cliente.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }.start();
    }//GEN-LAST:event_jButton1ActionPerformed

